void Ctoy4Dlg::Average(vector<int>& arr, CString& res)
{
	double sum = 0;
	for (int i = 0; i < arr.size(); i++)
	{
		sum += arr[i];
	}
	res.Format("%.2lf", sum /= arr.size());
	//AfxMessageBox(res);
}

